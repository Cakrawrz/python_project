# BELAJAR FOR LOOP
""""
FOR 
- digunakan untuk mengulang nilai berdasarkan blok kode yang di deklarasikan
- digunakan untuk melakukan perulangan berdasarkan jarak yang ditentukan
    dari urutan (list atau string)
"""

angka = [1, 2, 3, 4, 5]
for x in angka:
    print(x)

buah = ["Jeruk", "Belimbing", "Semangka", "Jambu"]
for makanan in buah:
    print("saya sukan makan", makanan)
    