#Belajar Tipe data

#Boolean 
#Menjelaskan dengan dua sifat antara True or False
# 1 atau 0


"""
String : bernilai karakter (bisa huruf atau angka) dan diapit tanda petik dua
Contoh : Cakra, 17, 07 februari 2001
"""

"""
Integer : Bernilai angka dan bilangan bulat
Note : tidak bisa angka bernilai koma
Contoh : 12 , 52 , 1002
"""

"""
Float : sebuah tipe data bernilai angka dan memiliki koma
Contoh : 3.14 , 0.99
"""

"""
Hexadecimal : sebuah tipeda yang memiliki bilangan hexa
bernilai 16 bit. Contoh : 9a , 1d3
"""

"""
Complex : Tipe data matematika yang menyatakan angka real dan Imajiner
Contoh : 1 + 5j
"""

"""
List : Tipe data yang digunakan untuk menyimpan beberapa data dan isinya dapat diubah
contoh : ['xyz', 784, 'cakra', 3.12]
"""

"""
Tuple : Tipe data yang digunakan untuk menyimpan data, tetapi tidak bisa diubah
Contoh :  ['xyz', 784, 'cakra', 3.12]
"""

"""
Dictionary : sebuah tipe data yang menyimpan beberapa variabel dan nilai
Contoh : {'nama', 'id', 'Nim'}
"""
