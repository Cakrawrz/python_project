#Operator Perbandingan : membandingkan antar kedua Nilai

# > Lebih dari
# < Kurang dari
# >= Lebih dari Sama dengan
# <= Kurang dari Sama dengan
# == Sama dengan
# != tidak sama dengan

print(1 > 1)
print(1 < 1)
print(1 >= 1)
print(1 <= 1)
print(1 == 1)
print(1 != 1)

