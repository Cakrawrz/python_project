#Operator Logika
#yang digunakan untuk menghasilkan nilai Boolen
"""
operasi dan   (and) note : kedua nilai harus memiliki kebenaran/True
operasi atau  (or)  note : salah satu nilai memiliki nilai True

Operasi Boolean hanya memiliki 4 kemungkinan
"""

print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)
